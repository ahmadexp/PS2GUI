; ============================================================================
;  diag_scamp : unlock + read + display the VL82C420 SCAMP config space.
;  The config is gated by port 0x22/0x23; it re-locks between bus cycles, so the
;  enable->read->disable is done atomically (interrupts off).  Read-only.
; ============================================================================
diag_scamp:
        call    cls_body_full
        mov     dx, 0x0000
        call    gotoxy
        mov     si, cs_title
        call    puts_tty
        ; --- atomic unlock -> read 0x74/0x76 idx 0x00-0x7F -> MEMBUF -> relock ---
        cli
        mov     dx, 0x23
        xor     al, al
        out     dx, al                  ; 0x23 <- 0
        mov     ax, 0x80
        mov     dx, 0x22
        out     dx, al                  ; 0x22 <- 0x80
        out     dx, ax                  ; 0x22 <- word 0x0080  (config access enabled)
        mov     di, MEMBUF
        xor     cl, cl
.rd:    mov     al, cl
        and     al, 0x7F
        mov     dx, 0x74
        out     dx, al
        mov     dx, 0x76
        in      al, dx
        mov     [di], al
        inc     di
        inc     cl
        cmp     cl, 0x80
        jb      .rd
        mov     dx, 0x22
        in      ax, dx
        and     ax, 0xFFFD
        or      ax, 0x100               ; bit8 = locked
        out     dx, ax                  ; config access disabled again
        sti
        ; --- display MEMBUF[0..0x7F] as 8 rows of 16 hex ---
        xor     bx, bx
.dl:    mov     ax, bx
        and     al, 0x0F
        jnz     .noNL
        mov     ax, bx
        mov     cl, 4
        shr     ax, cl
        add     al, 2                   ; screen row = 2 + (offset/16)
        mov     dh, al
        mov     dl, 2
        call    gotoxy
        mov     al, bl
        call    print_hex_al            ; offset label
        mov     al, ':'
        call    putc_tty
        mov     al, ' '
        call    putc_tty
.noNL:  mov     si, MEMBUF
        add     si, bx
        mov     al, [si]
        call    print_hex_al
        mov     al, ' '
        call    putc_tty
        inc     bx
        cmp     bx, 0x80
        jb      .dl
        mov     dh, 11
        mov     dl, 2
        call    gotoxy
        mov     si, cs_note
        call    puts_tty
        mov     dh, 13
        mov     dl, 2
        call    gotoxy
        mov     si, msg_donekey
        call    puts_hint
        call    getkey
        ret

; ============================================================================
;  diag_ptr : pointing-device (trackpad U75, NEC uPD17137A) identify + settings.
;  U75's only host interface is the PS/2 aux channel via the 8042 KBC, so this
;  talks raw 8042: enable aux, reset/identify, read status (resolution + sample
;  rate), and let the user change them live.  Restores the 8042 command byte on
;  exit.  (A loaded mouse driver re-inits the device, so changes are for testing.)
; ============================================================================
; aux_wait_ibf : spin until the 8042 input buffer is empty (status bit1=0)
aux_wait_ibf:
        push    ax
        push    cx
        xor     cx, cx
.w:     in      al, 0x64
        test    al, 0x02
        jz      .ok
        loop    .w
.ok:    pop     cx
        pop     ax
        ret
; aux_get : read one byte from the 8042 output buffer -> AL, CF=1 on timeout
aux_get:
        push    cx
        xor     cx, cx
.w:     in      al, 0x64
        test    al, 0x01
        jnz     .got
        loop    .w
        pop     cx
        stc
        ret
.got:   in      al, 0x60
        pop     cx
        clc
        ret
; aux_write : send AL to the aux device (0xD4 gate byte, then the data)
aux_write:
        push    ax
        call    aux_wait_ibf
        mov     al, 0xD4
        out     0x64, al
        call    aux_wait_ibf
        pop     ax
        out     0x60, al
        ret
; rd_cmdb : read the 8042 command byte -> AL (0x65 fallback on timeout)
rd_cmdb:
        call    aux_wait_ibf
        mov     al, 0x20
        out     0x64, al
        call    aux_get
        jnc     .ok
        mov     al, 0x65
.ok:    ret

ptr_status:                             ; query resolution + sample rate (0xE9)
        mov     al, 0xE9
        call    aux_write
        call    aux_get                 ; FA (ack)
        jc      .d
        call    aux_get                 ; status flags
        jc      .d
        call    aux_get                 ; resolution code
        jc      .d
        mov     [ptr_res], al
        call    aux_get                 ; sample rate
        jc      .d
        mov     [ptr_rate], al
.d:     ret

ptr_reset:                              ; reset (0xFF): FA, AA (BAT), device id
        mov     byte [ptr_ok], 0
        mov     byte [ptr_ridx], 5      ; reset -> default rate 100/s
        mov     al, 0xFF
        call    aux_write
        call    aux_get                 ; FA
        jc      .fail
        call    aux_get                 ; AA = self-test passed
        jc      .fail
        mov     [ptr_bat], al
        call    aux_get                 ; device id
        jc      .fail
        mov     [ptr_id], al
        mov     byte [ptr_ok], 1
        call    ptr_status
.fail:  ret

ptr_cycle_res:                          ; 0xE8 set-resolution, cycle 0..3
        mov     al, [ptr_res]
        inc     al
        and     al, 3
        mov     [ptr_res], al
        mov     al, 0xE8
        call    aux_write
        call    aux_get                 ; FA
        mov     al, [ptr_res]
        call    aux_write
        call    aux_get                 ; FA
        call    ptr_status
        ret

ptr_cycle_rate:                         ; 0xF3 set-sample-rate from pt_rates[]
        mov     al, [ptr_ridx]
        inc     al
        cmp     al, PT_NRATES
        jb      .ok
        xor     al, al
.ok:    mov     [ptr_ridx], al
        xor     ah, ah
        mov     bx, pt_rates
        add     bx, ax
        mov     al, [bx]
        push    ax
        mov     al, 0xF3
        call    aux_write
        call    aux_get                 ; FA
        pop     ax
        call    aux_write               ; rate value
        call    aux_get                 ; FA
        call    ptr_status
        ret

pt_print_res:                           ; AL = resolution code -> "N counts/mm"
        push    cx
        push    si
        mov     cl, al
        mov     al, 1
        shl     al, cl                  ; 0->1 1->2 2->4 3->8 counts/mm
        xor     ah, ah
        call    print_dec_ax
        mov     si, pt_cpermm
        call    puts_tty
        pop     si
        pop     cx
        ret

ptr_draw:
        call    cls_body_full
        mov     dx, 0x0000
        call    gotoxy
        mov     si, pt_title
        call    puts_tty
        cmp     byte [ptr_ok], 0
        je      .noresp
        mov     dh, 3
        mov     dl, 2
        call    gotoxy
        mov     si, pt_bat
        call    puts_tty
        mov     al, [ptr_bat]
        cmp     al, 0xAA
        mov     si, pt_pass
        je      .b1
        mov     si, pt_fail
.b1:    call    puts_tty
        mov     dh, 4
        mov     dl, 2
        call    gotoxy
        mov     si, pt_id
        call    puts_tty
        mov     al, [ptr_id]
        call    print_hex_al
        cmp     byte [ptr_id], 0
        jne     .res
        mov     si, pt_idstd
        call    puts_tty
.res:   mov     dh, 5
        mov     dl, 2
        call    gotoxy
        mov     si, pt_res
        call    puts_tty
        mov     al, [ptr_res]
        call    pt_print_res
        mov     dh, 6
        mov     dl, 2
        call    gotoxy
        mov     si, pt_rate
        call    puts_tty
        mov     al, [ptr_rate]
        xor     ah, ah
        call    print_dec_ax
        mov     si, pt_persec
        call    puts_tty
        jmp     .keys
.noresp:
        mov     dh, 3
        mov     dl, 2
        call    gotoxy
        mov     si, pt_nodev
        call    puts_tty
.keys:
        mov     dh, 9
        mov     dl, 2
        call    gotoxy
        mov     si, pt_keys1
        call    puts_tty
        mov     dh, 10
        mov     dl, 2
        call    gotoxy
        mov     si, pt_keys2
        call    puts_tty
        mov     dh, 12
        mov     dl, 2
        call    gotoxy
        mov     si, pt_note
        call    puts_tty
        ret

diag_ptr:
        call    rd_cmdb                 ; save current 8042 command byte
        mov     [ptr_cmdb], al
        call    aux_wait_ibf
        mov     al, 0xA8               ; enable aux device (mouse clock)
        out     0x64, al
        call    ptr_reset
.redraw:
        call    ptr_draw
.k:     call    getkey
        cmp     al, 0x1B
        je      .exit
        cmp     al, 'r'
        je      .cres
        cmp     al, 'R'
        je      .cres
        cmp     al, 's'
        je      .crate
        cmp     al, 'S'
        je      .crate
        cmp     al, 't'
        je      .ret
        cmp     al, 'T'
        je      .ret
        jmp     .k
.cres:  call    ptr_cycle_res
        jmp     .redraw
.crate: call    ptr_cycle_rate
        jmp     .redraw
.ret:   call    ptr_reset
        jmp     .redraw
.exit:
        ; restore the original 8042 command byte (re-disables aux)
        call    aux_wait_ibf
        mov     al, 0x60
        out     0x64, al
        call    aux_wait_ibf
        mov     al, [ptr_cmdb]
        out     0x60, al
        ret

; ============================================================================
;  Command builders. All produce:
;   - cmdtail (DOS EXEC command tail: count,"/C PS2 ...",CR)
;   - cmdshow (ASCIIZ "PS2 ..." for display)
; ============================================================================
; build_cmd_noopt: SI=cmd ptr -> "PS2 <cmd>"
build_cmd_noopt:
        mov     [bld_ptr], si     ; cmd_begin clobbers SI, so stash it
        call    cmd_begin
        mov     si, [bld_ptr]
        call    app_cmdptr
        jmp     cmd_end

; build_cmd_opt: SI=cmd ptr, [cur_opt]=opt ptr -> "PS2 <cmd> <opt>"
build_cmd_opt:
        mov     [bld_ptr], si
        call    cmd_begin
        mov     si, [bld_ptr]
        call    app_cmdptr
        mov     al, ' '
        call    app_char
        mov     si, [cur_opt]
        call    app_optval        ; append value part (after '=' if the option has one)
        jmp     cmd_end

; build_cmd_ultraopt: [cur_opt]=opt -> "<opt>" (for C:\ULTRACHG.COM <opt>)
build_cmd_ultraopt:
        call    cmd_begin
        mov     si, [cur_opt]
        call    app_optval
        jmp     cmd_end

; build_cmd_raw: SI=full arg string (e.g. "_@REVision") -> "PS2 <arg>"
build_cmd_raw:
        mov     [bld_ptr], si
        call    cmd_begin
        mov     si, [bld_ptr]
        call    app_cmdptr
        jmp     cmd_end

; helpers that build both the exec tail and the display string simultaneously
cmd_begin:
        ; display string starts with the show prefix ("PS2 " by default)
        mov     di, cmdshow
        mov     si, [show_pfx]
.cs:    lodsb
        mov     [di], al
        inc     di
        test    al, al
        jnz     .cs
        dec     di                ; back over the NUL
        mov     [show_ptr], di
        ; exec tail starts "/C PS2 "
        mov     di, cmdtail+1     ; leave room for count byte
        mov     si, pfx_cc
.ct:    lodsb
        test    al, al
        jz      .done
        mov     [di], al
        inc     di
        jmp     .ct
.done:
        mov     [tail_ptr], di
        ret

app_cmdptr:                       ; append ASCIIZ at SI to both buffers
.a:     lodsb
        test    al, al
        jz      .done
        call    app_char
        jmp     .a
.done:  ret

; app_optval : append an option's VALUE part to both buffers.  If the option
;              string contains '=', the value is what follows it; else the whole
;              string (SI = option string).
app_optval:
        push    bx
        mov     bx, si
.scan:  mov     al, [bx]
        test    al, al
        jz      .use              ; no '=' found -> use whole string (SI unchanged)
        cmp     al, '='
        je      .after
        inc     bx
        jmp     .scan
.after: inc     bx                ; skip '='
        mov     si, bx
.use:   pop     bx
        call    app_cmdptr
        ret

app_char:                         ; AL -> both buffers
        push    si
        mov     si, [show_ptr]
        mov     [si], al
        inc     si
        mov     [show_ptr], si
        mov     si, [tail_ptr]
        mov     [si], al
        inc     si
        mov     [tail_ptr], si
        pop     si
        ret

cmd_end:
        ; terminate display string
        mov     si, [show_ptr]
        mov     byte [si], 0
        ; terminate tail with CR and set count byte
        mov     si, [tail_ptr]
        mov     byte [si], 0x0D
        ; count = tail_ptr - (cmdtail+1)
        mov     ax, [tail_ptr]
        sub     ax, cmdtail+1
        mov     bx, cmdtail
        mov     [bx], al
        ret

; ============================================================================
;  do_exec : run COMMAND.COM with the built cmdtail
; ============================================================================
do_exec:
        push    ds
        push    es
        mov     [save_sp], sp
        mov     [save_ss], ss
        mov     ax, cs
        mov     ds, ax
        mov     es, ax
        mov     word [pb_env], 0
        mov     word [pb_tail_off], cmdtail
        mov     [pb_tail_seg], cs
        mov     word [pb_f1_off], 0x5C
        mov     [pb_f1_seg], cs
        mov     word [pb_f2_off], 0x6C
        mov     [pb_f2_seg], cs
        mov     dx, [exec_prog]
        mov     bx, parblk
        mov     ax, 0x4B00
        int     0x21
        cli
        mov     ss, [cs:save_ss]
        mov     sp, [cs:save_sp]
        sti
        pop     es
        pop     ds
        ret

; ============================================================================
;  Selection navigation over the rows[] table
; ============================================================================
; row_ptr: BX=row index -> SI = record address (7 bytes each)
row_ptr:
        mov     ax, bx
        mov     cx, 7
        mul     cx
        add     ax, rows
        mov     si, ax
        ret

; cat_ptr: BX=category index -> SI = category record (6 bytes)
cat_ptr:
        push    ax
        push    dx
        mov     ax, bx
        mov     dx, 6
        mul     dx
        add     ax, cats
        mov     si, ax
        pop     dx
        pop     ax
        ret

; scr_off: DH=row DL=col -> BX = B800 byte offset (AX/DX preserved)
scr_off:
        push    ax
        mov     al, dh
        mov     ah, 160
        mul     ah
        mov     bx, ax
        mov     al, dl
        xor     ah, ah
        shl     ax, 1
        add     bx, ax
        pop     ax
        ret

; put_cell: DH=row DL=col BL=char BH=attr (BX/DX/AX preserved)
put_cell:
        push    ax
        push    di
        push    es
        mov     al, dh
        mov     ah, 160
        mul     ah
        mov     di, ax
        mov     al, dl
        xor     ah, ah
        shl     ax, 1
        add     di, ax
        mov     ax, 0xB800
        mov     es, ax
        mov     [es:di], bl
        mov     [es:di+1], bh
        pop     es
        pop     di
        pop     ax
        ret

