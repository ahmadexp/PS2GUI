; read_cmos : AL = index (0..0x7F) -> AL = value.  Restores index to 0x0D.
read_cmos:
        push    cx
        cli
        out     0x70, al
        in      al, 0x71
        mov     cl, al
        mov     al, 0x0D
        out     0x70, al
        sti
        mov     al, cl
        pop     cx
        ret

; ============================================================================
;  BACKUP / RESTORE all settings via the CMOS config region (0x10-0x7F).
;  PS2/BIOS settings persist in CMOS, so a byte image of 0x10-0x7F captures
;  them all (incl. both CMOS checksums), self-consistently.  Restore writes it
;  back; the settings take effect on the next boot.
; ============================================================================
backup_settings:
        ; read CMOS 0x10..0x7F into the scratch buffer
        mov     di, MEMBUF
        mov     cl, 0x10
.rd:    mov     al, cl
        call    read_cmos
        mov     [di], al
        inc     di
        inc     cl
        cmp     cl, 0x80
        jb      .rd
        mov     si, msg_backup
        call    dump_header
        mov     dx, fn_set
        call    open_out
        jc      .err
        mov     bx, [fhandle]
        mov     dx, MEMBUF
        mov     cx, 0x70                ; 112 bytes (0x10..0x7F)
        mov     ah, 0x40
        int     0x21
        call    close_out
        mov     si, msg_bdone
        jmp     dump_finish
.err:   mov     si, msg_derr
        jmp     dump_finish

restore_settings:
        mov     si, msg_restore
        call    dump_header
        ; read the backup file into the scratch buffer
        mov     dx, fn_set
        mov     ax, 0x3D00              ; open for read
        int     0x21
        jc      .err
        mov     [fhandle], ax
        mov     bx, ax
        mov     dx, MEMBUF
        mov     cx, 0x70
        mov     ah, 0x3F
        int     0x21                    ; AX = bytes read
        push    ax
        call    close_out
        pop     ax
        cmp     ax, 0x70
        jne     .err                    ; not a valid full backup
        ; confirm before overwriting CMOS
        mov     dh, 3
        mov     dl, 0
        call    gotoxy
        mov     si, msg_rconf
        call    puts_tty
        call    getkey
        push    ax
        call    cls_body_full           ; clear the confirm prompt before the result
        mov     dx, 0x0000
        call    gotoxy
        mov     si, msg_restore
        call    puts_tty
        pop     ax
        cmp     al, 'y'
        je      .go
        cmp     al, 'Y'
        je      .go
        mov     si, msg_rcancel
        jmp     dump_finish
.go:
        mov     si, MEMBUF
        mov     cl, 0x10
.wr:    cli
        mov     al, cl
        out     0x70, al
        lodsb
        out     0x71, al
        sti
        inc     cl
        cmp     cl, 0x80
        jb      .wr
        mov     al, 0x0D
        out     0x70, al
        mov     si, msg_rdone
        jmp     dump_finish
.err:   mov     si, msg_derr
        jmp     dump_finish

; print_dec_al : print AL (0..255) as decimal via BIOS teletype, no leading zeros
print_dec_al:
        push    ax
        push    bx
        push    cx
        push    dx
        xor     ah, ah
        mov     bl, 100
        div     bl                ; AL=hundreds AH=rem
        mov     ch, al
        mov     al, ah
        xor     ah, ah
        mov     bl, 10
        div     bl                ; AL=tens AH=units
        mov     cl, al
        mov     dh, ah
        mov     byte [pd_go], 0
        mov     al, ch
        call    .dig
        mov     al, cl
        call    .dig
        mov     al, dh
        add     al, '0'
        call    putc_tty
        pop     dx
        pop     cx
        pop     bx
        pop     ax
        ret
.dig:   test    al, al
        jnz     .pr
        cmp     byte [pd_go], 0
        je      .skip
.pr:    mov     byte [pd_go], 1
        add     al, '0'
        call    putc_tty
.skip:  ret

; putc_tty : print AL via BIOS teletype
putc_tty:
        push    ax
        push    bx
        mov     ah, 0x0E
        mov     bx, [tty_col]
        int     0x10
        pop     bx
        pop     ax
        ret

; print_dec_ax : print AX (0..65535) decimal
print_dec_ax:
        push    ax
        push    bx
        push    cx
        push    dx
        mov     bx, 10
        xor     cx, cx
.d1:    xor     dx, dx
        div     bx
        push    dx
        inc     cx
        test    ax, ax
        jnz     .d1
.d2:    pop     dx
        mov     al, dl
        add     al, '0'
        call    putc_tty
        loop    .d2
        pop     dx
        pop     cx
        pop     bx
        pop     ax
        ret

; print_hex_al : print AL as two hex digits
print_hex_al:
        push    ax
        push    cx
        mov     ah, al
        mov     cl, 4
        shr     al, cl
        call    .nib
        mov     al, ah
        call    .nib
        pop     cx
        pop     ax
        ret
.nib:   and     al, 0x0F
        cmp     al, 10
        jb      .dig
        add     al, 'A'-10
        jmp     .pc
.dig:   add     al, '0'
.pc:    call    putc_tty
        ret

; ============================================================================
;  DUMPS : write ROM / BIOS / font images to files on the boot drive (DOS I/O).
; ============================================================================
open_out:                          ; DS:DX -> ASCIIZ name ; -> [fhandle], CF=err
        push    cx
        mov     ah, 0x3C
        xor     cx, cx
        int     0x21
        mov     [fhandle], ax
        pop     cx
        ret
close_out:
        mov     bx, [fhandle]
        mov     ah, 0x3E
        int     0x21
        ret
write_seg:                         ; BX=srcseg, CX=count -> file [fhandle]
        push    ds
        mov     ds, bx
        xor     dx, dx
        mov     bx, [cs:fhandle]
        mov     ah, 0x40
        int     0x21
        pop     ds
        ret
dump_region:                       ; [dseg]=start seg, [dchunks]=# of 4 KB chunks
.l:     cmp     word [dchunks], 0
        je      .done
        mov     bx, [dseg]
        mov     cx, 0x1000
        call    write_seg
        add     word [dseg], 0x100
        dec     word [dchunks]
        jmp     .l
.done:  ret

dump_header:                       ; SI = title
        push    si
        call    cls_body_full
        mov     dx, 0x0000
        call    gotoxy
        pop     si
        call    puts_tty
        ret
dump_finish:                       ; SI = result msg
        push    si
        mov     dh, 3
        mov     dl, 0
        call    gotoxy
        pop     si
        call    puts_tty
        mov     dh, 5
        mov     dl, 0
        call    gotoxy
        mov     si, msg_donekey
        call    puts_hint
        call    getkey
        ret

dump_bios:
        mov     si, msg_dbios
        call    dump_header
        mov     dx, fn_bios
        call    open_out
        jc      .err
        mov     word [dseg], 0xF000
        mov     word [dchunks], 16      ; 64 KB
        call    dump_region
        call    close_out
        mov     si, msg_ddone
        jmp     dump_finish
.err:   mov     si, msg_derr
        jmp     dump_finish

dump_vbios:
        mov     si, msg_dvbios
        call    dump_header
        mov     dx, fn_vbios
        call    open_out
        jc      .err
        mov     word [dseg], 0xC000
        mov     word [dchunks], 8       ; 32 KB
        call    dump_region
        call    close_out
        mov     si, msg_ddone
        jmp     dump_finish
.err:   mov     si, msg_derr
        jmp     dump_finish

dump_font:
        mov     si, msg_dfont
        call    dump_header
        mov     dx, fn_font
        call    open_out
        jc      .err
        push    bp
        xor     bp, bp                  ; bank 0..127
.bl:    mov     ax, bp
        mov     dx, 0x1160
        out     dx, al                  ; bank select
        mov     al, 0xDE
        mov     dx, 0x1162
        out     dx, al                  ; window segment high
        mov     al, 0x01
        mov     dx, 0x1163
        out     dx, al                  ; enable window
        mov     bx, 0xDE00
        mov     cx, 0x2000              ; 8 KB window
        call    write_seg
        inc     bp
        cmp     bp, 128
        jb      .bl
        xor     al, al
        mov     dx, 0x1160
        out     dx, al                  ; restore bank 0
        pop     bp
        call    close_out
        mov     si, msg_ddone
        jmp     dump_finish
.err:   mov     si, msg_derr
        jmp     dump_finish

