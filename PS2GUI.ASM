; ============================================================================
;  Program entry — the graphical IBM Easy-Setup launcher (VGA mode 12h)
; ============================================================================
; layout / palette-index constants for the graphical menu
C_MAUVE   equ 0
C_WHITE   equ 1
C_RED     equ 2
C_NAVY    equ 3
C_BLACK   equ 4
ITEMROW0  equ 4               ; first list row (text rows are 8x16, 80x30)

; ============================================================================
;  Program entry — graphical menu (VGA mode 12h, Easy-Setup style).
;  Main menu = PS2TUI's 10 categories; sub-menus and pickers are graphical.
; ============================================================================
start:
        mov     ax, cs
        mov     ds, ax
        mov     es, ax
        mov     bx, 0x1000
        mov     ah, 0x4A
        int     0x21
        ; --- refuse to run on non-PC110 hardware (QEMU allowed, for testing) ---
        call    is_pc110          ; CF clear = genuine PC110
        jnc     .hwok
        call    is_qemu           ; CF clear = QEMU
        jnc     .hwok
        call    hw_warn           ; CF set = user chose to quit
        jnc     .hwok
        mov     ax, 0x4C01        ; abort, exit code 1
        int     0x21
.hwok:
        mov     ax, 0x0012        ; VGA 640x480x16
        int     0x10
        call    set_palette
        push    es                ; cache the ROM 8x16 font pointer for gtext
        mov     ax, 0x1130
        mov     bh, 6
        int     0x10              ; -> ES:BP = 8x16 font
        mov     [font_seg], es
        mov     [font_off], bp
        pop     es
        call    mouse_init
        mov     word [gcat], 0
.kflush:                              ; drop any stale keystroke (e.g. the ESC that
        mov     ah, 1                 ; quit a previous run) so we don't insta-quit
        int     0x16
        jz      .kdone
        xor     ah, ah
        int     0x16
        jmp     .kflush
.kdone:
        call    g_draw_main
.mloop:
        mov     ax, [gcat]
        mov     [gsel_old], ax       ; remember selection for incremental redraw
        call    wait_event
        cmp     byte [ev], 1
        je      .mclick
        mov     ax, [ev_key]
        cmp     al, 0x1B
        je      .mquit
        cmp     al, 0x0D
        je      .mopen
        cmp     al, 0
        jne     .mloop
        cmp     ah, 0x48
        je      .mup
        cmp     ah, 0x50
        je      .mdown
        cmp     ah, 0x4B
        je      .mleft
        cmp     ah, 0x4D
        je      .mright
        jmp     .mloop
.mup:
        mov     ax, [gcat]
        cmp     ax, GCOLS
        jb      .mred
        sub     ax, GCOLS
        mov     [gcat], ax
        jmp     .mred
.mdown:
        mov     ax, [gcat]
        add     ax, GCOLS
        cmp     ax, NCATS
        jae     .mred
        mov     [gcat], ax
        jmp     .mred
.mleft:
        cmp     word [gcat], 0
        je      .mred
        dec     word [gcat]
        jmp     .mred
.mright:
        mov     ax, [gcat]
        inc     ax
        cmp     ax, NCATS
        jae     .mred
        mov     [gcat], ax
.mred:
        call    cursor_hide          ; repaint only the two affected tiles
        mov     ax, [gsel_old]
        mov     [tile_i], ax
        mov     byte [tile_sel], 0
        call    main_tile
        mov     ax, [gcat]
        mov     [tile_i], ax
        mov     byte [tile_sel], 1
        call    main_tile
        jmp     .mloop
.mclick:
        call    hit_tile
        jc      .mloop
        cmp     ax, NCATS
        jae     .mloop
        mov     [gcat], ax           ; click a category tile -> open it
.mopen:
        mov     word [gitem], 0
        call    g_sub_loop
        call    g_draw_main
        jmp     .mloop
.mquit:
        call    mouse_shutdown       ; release IRQ12 / restore 8042 (direct mode)
        mov     ax, 0x0003
        int     0x10
        mov     ax, 0x4C00
        int     0x21

; ============================================================================
;  Hardware guard: PS2GUI drives PC110-specific hardware directly, so it must
;  only run on a genuine PC110 (QEMU is allowed so it can be developed/tested).
; ============================================================================
; is_pc110 : CF clear if the IBM PC110 APM vendor BIOS answers with its "SLO"
;   identify signature (same check PS2.EXE / ULTRACHG use).  CF set otherwise.
is_pc110:
        push    ax
        push    bx
        push    cx
        mov     ax, 0x5380           ; AH=53h APM, AL=80h IBM vendor extension
        mov     bh, 0x7F             ; function 7F = identify
        int     0x15
        cmp     bh, 0x53             ; 'S'
        jne     .no
        cmp     bl, 0x4C             ; 'L'
        jne     .no
        cmp     cl, 0x4F             ; 'O'
        jne     .no
        clc
        jmp     .done
.no:    stc
.done:  pop     cx
        pop     bx
        pop     ax
        ret

; is_qemu : CF clear if the QEMU fw_cfg port (0x510/0x511) returns "QEMU".
is_qemu:
        push    ax
        push    dx
        mov     dx, 0x510            ; fw_cfg selector
        xor     ax, ax               ; entry 0 = signature
        out     dx, ax
        mov     dx, 0x511            ; fw_cfg data
        in      al, dx
        cmp     al, 'Q'
        jne     .no
        in      al, dx
        cmp     al, 'E'
        jne     .no
        in      al, dx
        cmp     al, 'M'
        jne     .no
        in      al, dx
        cmp     al, 'U'
        jne     .no
        clc
        jmp     .done
.no:    stc
.done:  pop     dx
        pop     ax
        ret

; hw_warn : this is not a PC110 (or QEMU).  Show the warning in text mode and
;   wait: ESC quits (CF set); F10 runs anyway (CF clear).
hw_warn:
        mov     ax, 0x0003           ; 80x25 text mode (clears screen)
        int     0x10
        mov     ah, 0x09
        mov     dx, warn_msg
        int     0x21
.wk:    xor     ah, ah
        int     0x16
        cmp     al, 0x1B             ; ESC -> quit (recommended)
        je      .quit
        cmp     ah, 0x44             ; F10 -> run anyway
        je      .run
        jmp     .wk
.run:   clc
        ret
.quit:  stc
        ret

; ---- sub-menu (items of category [gcat]) ----
g_sub_loop:
        call    g_draw_sub
.sloop:
        mov     ax, [gitem]
        mov     [gsel_old], ax       ; remember selection for incremental redraw
        call    wait_event
        cmp     byte [ev], 1
        je      .sclick
        mov     ax, [ev_key]
        cmp     al, 0x1B
        je      .sback
        cmp     al, 0x0D
        je      .sopen
        cmp     al, 0
        jne     .sloop
        cmp     ah, 0x48
        je      .sup
        cmp     ah, 0x50
        je      .sdown
        cmp     ah, 0x4B
        je      .sleft
        cmp     ah, 0x4D
        je      .sright
        jmp     .sloop
.sclick:
        call    hit_tile
        jc      .sloop
        mov     bx, ax
        mov     al, [cat_count]
        xor     ah, ah
        cmp     bx, ax
        jae     .sloop
        mov     [gitem], bx          ; click an item tile -> activate it
        call    g_activate
        call    g_draw_sub
        jmp     .sloop
.sup:
        mov     ax, [gitem]
        cmp     ax, GCOLS
        jb      .sred
        sub     ax, GCOLS
        mov     [gitem], ax
        jmp     .sred
.sdown:
        mov     ax, [gitem]
        add     ax, GCOLS
        mov     bl, [cat_count]
        xor     bh, bh
        cmp     ax, bx
        jae     .sred
        mov     [gitem], ax
        jmp     .sred
.sleft:
        cmp     word [gitem], 0
        je      .sred
        dec     word [gitem]
        jmp     .sred
.sright:
        mov     ax, [gitem]
        inc     ax
        mov     bl, [cat_count]
        xor     bh, bh
        cmp     ax, bx
        jae     .sred
        mov     [gitem], ax
.sred:
        call    cursor_hide          ; repaint only the two affected tiles
        mov     ax, [gsel_old]
        mov     [tile_i], ax
        mov     byte [tile_sel], 0
        call    sub_tile
        mov     ax, [gitem]
        mov     [tile_i], ax
        mov     byte [tile_sel], 1
        call    sub_tile
        jmp     .sloop
.sopen:
        call    g_activate
        call    g_draw_sub
        jmp     .sloop
.sback:
        ret

; ---- activate the selected item (dispatch by row type) ----
g_activate:
        mov     al, [cat_start]
        xor     ah, ah
        add     ax, [gitem]
        mov     [sel], ax
        mov     bx, ax
        call    row_ptr
        mov     al, [si+6]
        cmp     al, R_ITEM
        je      .item
        cmp     al, R_UITEM
        je      .uitem
        cmp     al, R_ACT
        je      .act
        cmp     al, R_INFO
        je      .info
        mov     ax, [si+2]        ; R_CALL -> self-contained test/report
        push    ax
        call    to_text           ; mouse off + mask IRQ12 (8042/keyboard safety)
        mov     ax, 0x0012        ; run it on a GRAPHICAL mauve page (Easy-Setup look)
        int     0x10
        call    set_palette
        mov     byte [body_gfx], 1
        mov     word [tty_col], 0x0004   ; dark text
        pop     ax
        call    ax
        mov     byte [body_gfx], 0
        mov     word [tty_col], 0x0007
        call    to_gfx
        ret
.info:
        call    to_text
        call    run_revision
        call    to_gfx
        ret
.act:
        mov     si, [si+2]
        call    build_cmd_noopt
        call    to_text
        call    confirm_run
        call    to_gfx
        ret
.item:
        mov     ax, [si+2]
        mov     [cur_cmd], ax
        mov     ax, [si+4]
        mov     [opt_base], ax
        call    g_picker
        jc      .done
        call    opt_ptr
        mov     [cur_opt], si
        mov     si, [cur_cmd]
        call    build_cmd_opt
        call    to_text
        call    confirm_run
        call    to_gfx
.done:
        ret
.uitem:
        mov     word [exec_prog], ultra_prog
        mov     word [show_pfx], pfx_ultra
        mov     byte [ultra_mode], 1
        mov     ax, [si+4]
        mov     [opt_base], ax
        call    g_picker
        jc      .ureset
        call    opt_ptr
        mov     [cur_opt], si
        call    build_cmd_ultraopt
        call    to_text
        call    confirm_run
        call    to_gfx
.ureset:
        mov     word [exec_prog], progname
        mov     word [show_pfx], pfx_ps2
        mov     byte [ultra_mode], 0
        ret

; ---- graphical value picker (options of [opt_base]); CF=0 chosen / CF=1 cancel ----
g_picker:
        mov     word [opt_sel], 0
        call    g_draw_pick
.ploop:
        mov     ax, [opt_sel]
        mov     [gsel_old], ax       ; remember selection for incremental redraw
        call    wait_event
        cmp     byte [ev], 1
        je      .pclick
        mov     ax, [ev_key]
        cmp     al, 0x1B
        je      .pcancel
        cmp     al, 0x0D
        je      .pok
        cmp     al, 0
        jne     .ploop
        cmp     ah, 0x48
        je      .pup
        cmp     ah, 0x50
        je      .pdown
        jmp     .ploop
.pclick:
        call    hit_opt
        jc      .ploop
        mov     [opt_sel], ax        ; click a value -> choose it
        clc
        ret
.pup:
        cmp     word [opt_sel], 0
        je      .pred
        dec     word [opt_sel]
        jmp     .pred
.pdown:
        call    opt_count
        mov     ax, [opt_sel]
        inc     ax
        cmp     ax, cx
        jae     .pred
        mov     [opt_sel], ax
.pred:
        call    cursor_hide          ; repaint only the two affected option rows
        mov     ax, [gsel_old]
        mov     [tile_i], ax
        mov     byte [tile_sel], 0
        call    pick_row
        mov     ax, [opt_sel]
        mov     [tile_i], ax
        mov     byte [tile_sel], 1
        call    pick_row
        jmp     .ploop
.pok:
        clc
        ret
.pcancel:
        stc
        ret

; pick_row : repaint one picker option row [tile_i] with [tile_sel]
pick_row:
        mov     ax, [tile_i]
        mov     [pk_i], ax
        mov     bx, PK_ROW
        mul     bx
        add     ax, [pk_y]
        add     ax, PK_TITLE + 4
        mov     [pk_oy], ax
        mov     word [fr_x], PK_X + 8
        mov     ax, [pk_oy]
        dec     ax
        mov     [fr_y], ax
        mov     word [fr_w], PK_W - 16
        mov     word [fr_h], PK_ROW
        mov     al, C_WHITE
        cmp     byte [tile_sel], 0
        je      .bg
        mov     al, C_RED
.bg:    mov     [fr_col], al
        call    g_fillrect
        mov     byte [g_col], C_NAVY
        cmp     byte [tile_sel], 0
        je      .t
        mov     byte [g_col], C_WHITE
.t:     call    ptr_to_opt
        push    si
        call    gt_strlen
        pop     si
        shl     cx, 2
        mov     ax, PK_X + PK_W/2
        sub     ax, cx
        mov     [gt_x], ax
        mov     ax, [pk_oy]
        add     ax, 2
        mov     [gt_y], ax
        call    gtext
        ret

; ---- graphical draw helpers ----
g_fill_mauve:
        push    es
        mov     dx, 0x3C4
        mov     al, 2
        out     dx, al
        inc     dx
        mov     al, 0x0F
        out     dx, al
        mov     ax, 0xA000
        mov     es, ax
        xor     di, di
        mov     cx, 19200
        xor     ax, ax
        rep     stosw
        pop     es
        ret

; g_fill_page : white screen (like Easy-Setup's border) with the mauve panel
; inset at (32,32)-(607,400); everything else is drawn on the panel.
g_fill_page:
        mov     byte [cur_valid], 0  ; whole screen repainted -> cursor bg is stale
        mov     word [fr_x], 0
        mov     word [fr_y], 0
        mov     word [fr_w], 640
        mov     word [fr_h], 480
        mov     byte [fr_col], C_WHITE
        call    g_fillrect
        mov     word [fr_x], 32
        mov     word [fr_y], 32
        mov     word [fr_w], 576
        mov     word [fr_h], 369
        mov     byte [fr_col], C_MAUVE
        call    g_fillrect
        ret

; g_bg : prepare the background for a menu/dialog draw.  The white border, mauve
; panel, title and copyright are the same on every screen, so on a normal screen
; change we DON'T repaint them (that full white fill is what flashes) - we only
; clear the content region and refresh the breadcrumb.  A full page is drawn only
; the first time and after a video-mode reset ([need_full]=1, set by to_gfx).
g_bg:
        cmp     byte [need_full], 0
        je      .content
        call    g_fill_page
        call    g_chrome
        mov     byte [need_full], 0
        ret
.content:
        mov     byte [cur_valid], 0  ; content repainted -> cursor bg is stale
        mov     word [fr_x], 40      ; clear only the content area to mauve
