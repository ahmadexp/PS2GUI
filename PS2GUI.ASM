;  GRAPHICS  (VGA mode 12h — the Easy-Setup screen)
; ============================================================================
; gui_show : enter mode 12h, load the Easy-Setup palette, blit the screen and
;            draw the selection box on the current icon.
gui_show:
        mov     ax, 0x0012
        int     0x10
        call    set_palette
        call    blit_screen
        mov     al, HL_COLOR
        call    draw_ring
        ret

; set DAC entries 0..PAL_N-1 from the espal table (r,g,b 6-bit)
set_palette:
        mov     si, espal
        xor     bx, bx
.pl:    mov     dh, [si]
        mov     ch, [si+1]
        mov     cl, [si+2]
        mov     ax, 0x1010
        int     0x10
        add     si, 3
        inc     bx
        cmp     bx, PAL_N
        jb      .pl
        ret

; blit the 4 RLE-compressed bit-planes into A000 (map-mask per plane)
blit_screen:
        push    es
        mov     ax, 0xA000
        mov     es, ax
        xor     bp, bp
.pl:
        mov     dx, 0x3C4                ; Sequencer Map Mask = 1<<plane
        mov     al, 0x02
        out     dx, al
        inc     dx
        mov     bx, bp
        mov     al, [mm_tab+bx]
        out     dx, al
        mov     bx, bp
        shl     bx, 1
        mov     si, [esplanes+bx]
        xor     di, di
.dec:   lodsb
        mov     ah, al
        lodsb
        mov     cl, ah
        xor     ch, ch
        rep     stosb
        cmp     di, 38400               ; 80 bytes/row * 480 rows
        jb      .dec
        inc     bp
        cmp     bp, 4
        jb      .pl
        mov     dx, 0x3C4               ; re-enable all planes
        mov     al, 0x02
        out     dx, al
        inc     dx
        mov     al, 0x0F
        out     dx, al
        pop     es
        ret

; hl_erase : redraw the selection ring in mauve (palette idx 0)
hl_erase:
        xor     al, al
; draw_ring : AL = colour ; 3-px box in the mauve gap around icon [isel]
draw_ring:
        mov     [ringcol], al
        mov     bx, [isel]
        shl     bx, 3                    ; 8 bytes per icon record
        mov     ax, [icons+bx]
        mov     [bx0], ax
        mov     ax, [icons+bx+2]
        mov     [by0], ax
        mov     ax, [icons+bx+4]
        mov     [bx1], ax
        mov     ax, [icons+bx+6]
        mov     [by1], ax
        mov     byte [ringm], 3
.ring:
        mov     al, [ringm]
        cbw
        mov     cx, ax
        mov     ax, [bx0]
        sub     ax, cx
        mov     [rx0], ax
        mov     ax, [by0]
        sub     ax, cx
        mov     [ry0], ax
        mov     ax, [bx1]
        add     ax, cx
        mov     [rx1], ax
        mov     ax, [by1]
        add     ax, cx
        mov     [ry1], ax
        mov     dx, [ry0]
        call    hline
        mov     dx, [ry1]
        call    hline
        mov     cx, [rx0]
        call    vline
        mov     cx, [rx1]
        call    vline
        inc     byte [ringm]
        cmp     byte [ringm], 6
        jb      .ring
        ret

hline:
        push    dx
        mov     cx, [rx0]
.h:     mov     al, [ringcol]
        call    putpixel
        inc     cx
        cmp     cx, [rx1]
        jbe     .h
        pop     dx
        ret
vline:
        push    cx
        mov     dx, [ry0]
.v:     mov     al, [ringcol]
        call    putpixel
        inc     dx
        cmp     dx, [ry1]
        jbe     .v
        pop     cx
        ret
putpixel:
        push    ax
        push    bx
        mov     ah, 0x0C
        xor     bh, bh
        int     0x10
        pop     bx
        pop     ax
        ret

