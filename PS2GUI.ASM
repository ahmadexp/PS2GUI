A_BOX     equ 0x1F        ; popup dialogs: white on blue (pop against cyan)
A_WARN    equ 0x4F        ; white on red
A_DESK    equ 0x30        ; window/desktop fill: black on cyan (space = cyan)

; ---- row types -------------------------------------------------------------
