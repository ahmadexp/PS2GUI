        cmp     byte [body_gfx], 0
        jne     .gfx
        push    ax
        push    bx
        push    cx
        push    dx
        mov     ah, 0x03          ; get cursor position -> DH=row, DL=col
        xor     bh, bh
        int     0x10
        call    scr_off           ; DH,DL -> BX (B800 offset)
        mov     ah, 0x1B          ; cyan on blue
        call    puts_at
        pop     dx
        pop     cx
        pop     bx
        pop     ax
        ret
