.d:     ret

; g_tile : draw tile [tile_i] (bg + icon SI), highlighted if [tile_sel]
g_tile:
        mov     ax, [tile_i]
        mov     bl, GCOLS
        div     bl                   ; al=row ah=col
        mov     [t_row], al
        mov     [t_col], ah
        mov     al, [t_col]          ; tx = col*104 + 80  (exact Easy-Setup grid)
        xor     ah, ah
        mov     bx, 104
        mul     bx
        add     ax, 80
        mov     [tx], ax
        mov     al, [t_row]          ; ty = row*112 + 160
        xor     ah, ah
        mov     bx, 112
        mul     bx
        add     ax, 160
        mov     [ty], ax
        mov     ax, [tx]             ; tile background
        mov     [fr_x], ax
        mov     ax, [ty]
        mov     [fr_y], ax
        mov     word [fr_w], 64
        mov     word [fr_h], 64
        mov     al, C_WHITE
        cmp     byte [tile_sel], 0
        je      .bg
        mov     al, C_RED
.bg:    mov     [fr_col], al
        call    g_fillrect
        mov     byte [g_col], C_RED  ; icon colour (inverted when selected)
        cmp     byte [tile_sel], 0
        je      .ic
        mov     byte [g_col], C_WHITE
.ic:    mov     ax, [tx]
        add     ax, 8
        mov     [bm_x], ax
        mov     ax, [ty]
        add     ax, 8
        mov     [bm_y], ax
        mov     word [bm_ww], 3
        mov     word [bm_h], 48
        call    draw_bmp
        ret

; g_tile_label : SI=label, positioned under tile [tile_i]
g_tile_label:
        mov     ax, [tile_i]
        mov     bl, GCOLS
        div     bl                   ; al=row ah=col
        mov     [t_row], al
        mov     [t_col], ah
        mov     al, [t_col]          ; centre col = col*13 + 14  ((tx+32)/8)
        xor     ah, ah
        mov     bx, 13
        mul     bx
        add     ax, 14
        mov     [lbl_cc], ax
        mov     al, [t_row]          ; label row = (row*112 + 226) / 16
        xor     ah, ah
        mov     bx, 112
        mul     bx
        add     ax, 226
        mov     cl, 4
        shr     ax, cl
        mov     [lbl_r0], al
        mov     byte [g_col], C_BLACK
        call    g_label
        ret

; g_fillrect : planar fill [fr_x](byte-aligned),[fr_y],[fr_w](mult 8),[fr_h] = [fr_col]
g_fillrect:
        push    es
        cld
        mov     ax, 0xA000
        mov     es, ax
        xor     bp, bp               ; plane 0..3
.pl:    mov     dx, 0x3C4
        mov     al, 2
        out     dx, al
        inc     dx
        mov     cx, bp
        mov     al, 1
        shl     al, cl
        out     dx, al               ; map mask = 1<<plane
        mov     al, [fr_col]
        mov     cx, bp
        shr     al, cl
        and     al, 1
        neg     al                   ; bit set -> 0xFF else 0x00
        mov     [fr_fill], al
        mov     cx, [fr_h]
        mov     bx, [fr_y]
.row:   push    cx
        mov     ax, bx
        mov     cx, 80
        mul     cx                   ; ax = y*80
        mov     di, ax
        mov     ax, [fr_x]
        shr     ax, 1
        shr     ax, 1
        shr     ax, 1                ; /8
        add     di, ax
        mov     cx, [fr_w]
        shr     cx, 1
        shr     cx, 1
        shr     cx, 1                ; bytes = w/8
        mov     al, [fr_fill]
        rep     stosb
        pop     cx
        inc     bx
        loop    .row
        inc     bp
        cmp     bp, 4
        jb      .pl
        mov     dx, 0x3C4            ; restore map mask = all planes
        mov     al, 2
        out     dx, al
        inc     dx
        mov     al, 0x0F
        out     dx, al
        pop     es
        ret

; draw_bmp : SI=bitmap, [bm_x]/[bm_y], [bm_ww] words/row, [bm_h] rows;
;            plots set bits in [g_col], clear bits untouched.
draw_bmp:
        pusha
        mov     word [bm_r], 0
.rl:    mov     ax, [bm_r]
        cmp     ax, [bm_h]
        jae     .done
        mov     ax, [bm_r]
        mov     bx, [bm_ww]
        mul     bx
        shl     ax, 1                ; r*ww*2 bytes
        mov     di, si
        add     di, ax               ; di -> row words
        mov     word [bm_w], 0
.wl:    mov     ax, [bm_w]
        cmp     ax, [bm_ww]
        jae     .rnext
        mov     bx, [bm_w]
        shl     bx, 1
        mov     dx, [di+bx]          ; 16 bits
        mov     word [bm_c], 0
.bl:    cmp     word [bm_c], 16
        jae     .wnext
        shl     dx, 1                ; MSB -> CF
        jnc     .skip
        push    dx
        mov     cx, [bm_x]
        mov     ax, [bm_w]
        shl     ax, 4                ; w*16
        add     cx, ax
        add     cx, [bm_c]
        mov     dx, [bm_y]
        add     dx, [bm_r]
        mov     al, [g_col]
        call    putpixel
        pop     dx
.skip:  inc     word [bm_c]
        jmp     .bl
.wnext: inc     word [bm_w]
        jmp     .wl
.rnext: inc     word [bm_r]
        jmp     .rl
.done:  popa
        ret

; strlen_si : CX = length of asciiz at SI (SI preserved)
strlen_si:
        push    si
        xor     cx, cx
.s:     cmp     byte [si], 0
        je      .d
        inc     si
        inc     cx
        jmp     .s
.d:     pop     si
        ret

; g_putn : draw CX chars from SI at (DH,DL) in [g_col]
g_putn:
        push    si
        mov     [pn_n], cx
        mov     ah, 2
        xor     bh, bh
        int     0x10
.l:     cmp     word [pn_n], 0
        je      .d
        lodsb
        mov     ah, 0x0E
        mov     bl, [g_col]
        xor     bh, bh
        int     0x10
        dec     word [pn_n]
        jmp     .l
.d:     pop     si
        ret

; gt_strlen : CX = chars at SI until NUL or '=' (SI preserved)
gt_strlen:
        push    si
        xor     cx, cx
.s:     mov     al, [si]
        test    al, al
        je      .d
        cmp     al, '='
        je      .d
        inc     si
        inc     cx
        jmp     .s
.d:     pop     si
        ret

; gtext : draw asciiz SI at pixel [gt_x],[gt_y] in [g_col] using the ROM 8x16
; font, transparent background (only glyph pixels set). Stops at NUL or '='.
gtext:
        pusha
        push    es
        mov     es, [font_seg]
        mov     word [gt_cx], 0
.ch:    lodsb
        test    al, al
        je      .done
        cmp     al, '='
        je      .done
        xor     ah, ah               ; glyph = font_off + al*16
        shl     ax, 1
        shl     ax, 1
        shl     ax, 1
        shl     ax, 1
        add     ax, [font_off]
        mov     di, ax
        xor     bp, bp               ; row 0..15
.row:   mov     bl, [es:di]          ; font byte for this row
        xor     bh, bh               ; bit 0..7
.bit:   mov     al, 0x80
        mov     cl, bh
        shr     al, cl
        test    bl, al
        je      .nb
        mov     cx, [gt_cx]          ; x = gt_x + col*8 + bit
        shl     cx, 3
        add     cx, [gt_x]
        mov     al, bh
        xor     ah, ah
        add     cx, ax
        mov     dx, bp               ; y = gt_y + row
        add     dx, [gt_y]
        mov     al, [g_col]
        call    putpixel
.nb:    inc     bh
        cmp     bh, 8
        jb      .bit
        inc     di
        inc     bp
        cmp     bp, 16
        jb      .row
        inc     word [gt_cx]
        jmp     .ch
.done:  pop     es
        popa
        ret

; g_label : SI=asciiz, centred at column [lbl_cc], starting row [lbl_r0].
;           Greedy word-wrap to up to 3 lines of at most MAXW chars (so labels
;           never spill into the neighbouring 5-column tiles).
MAXW      equ 12
g_label:
        mov     [ls_p], si           ; line start
        mov     [ws_p], si           ; next word start
        mov     word [ll_cur], 0
        mov     al, [lbl_r0]
        mov     [cur_row], al
        mov     byte [nlines], 0
.word:
        mov     si, [ws_p]
        cmp     byte [si], 0
        je      .last
        mov     di, si               ; scan to end of word
.we:    mov     al, [di]
        cmp     al, 0
        je      .weend
        cmp     al, ' '
        je      .weend
        inc     di
        jmp     .we
.weend:
        mov     [we_p], di
        mov     ax, di               ; wl = we - word start
        sub     ax, si
        mov     [wl_cur], ax
        cmp     word [ll_cur], 0
        jne     .extend
        mov     [ll_cur], ax         ; first word on the line
        mov     [ls_p], si
        jmp     .adv
.extend:
        mov     ax, [we_p]           ; cand = we - line start
        sub     ax, [ls_p]
        cmp     ax, MAXW
        ja      .flush
        mov     [ll_cur], ax
        jmp     .adv
.flush:
        call    flush_line
        mov     ax, [ws_p]           ; new line begins at this word
        mov     [ls_p], ax
        mov     ax, [wl_cur]
        mov     [ll_cur], ax
.adv:
        mov     ax, [we_p]
        mov     [ws_p], ax
        mov     si, ax
        cmp     byte [si], ' '
        jne     .word
        inc     word [ws_p]
        jmp     .word
.last:
        cmp     word [ll_cur], 0
        je      .done
        call    flush_line
.done:
        ret

flush_line:                          ; draw [ls_p] (len [ll_cur]) centred at [cur_row]
        cmp     byte [nlines], 3
        jae     .sk
        mov     ax, [lbl_cc]
        mov     bx, [ll_cur]
        shr     bx, 1
        sub     ax, bx
        mov     dl, al
        mov     al, [cur_row]
        mov     dh, al
        mov     cx, [ll_cur]
        mov     si, [ls_p]
        call    g_putn
        inc     byte [cur_row]
.sk:    inc     byte [nlines]
        ret


; ============================================================================
;  activate_sel : act on the currently selected row
; ============================================================================
activate_sel:
        mov     bx, [sel]
        call    row_ptr           ; ES:BX invalid; use DS. returns SI=record
        mov     al, [si+6]        ; type
        cmp     al, R_INFO
        je      run_revision
        cmp     al, R_ACT
        je      .action
        cmp     al, R_CALL
        je      .call
        cmp     al, R_UITEM
        je      .uitem
        ; R_ITEM : open the option picker
        jmp     pick_option
.uitem:
        ; charging item: exec C:\ULTRACHG.COM <option> instead of PS2.EXE
        mov     word [exec_prog], ultra_prog
        mov     word [show_pfx], pfx_ultra
        mov     byte [ultra_mode], 1
        jmp     pick_option
.action:
        ; build "PS2 <cmd>" and confirm
        mov     si, [si+2]        ; cmd ptr
        call    build_cmd_noopt
        call    confirm_run
        ret
.call:
        ; internal routine: cmd field = its offset
        mov     ax, [si+2]
        call    ax
        ret

; ============================================================================
;  pick_option : popup list of options for the selected item, run PS2 <cmd> opt
; ============================================================================
pick_option:
        mov     bx, [sel]
        call    row_ptr           ; SI=record
        mov     ax, [si+2]
        mov     [cur_cmd], ax     ; save cmd ptr
        mov     ax, [si+4]
        mov     [opt_base], ax    ; options list base
        mov     word [opt_sel], 0

.redraw:
        call    draw_picker
.pk:
        call    getkey
        cmp     al, 0x1B
        je      .cancel
        cmp     al, 0
        jne     .pk_ascii
        cmp     ah, 0x48
        je      .pk_up
        cmp     ah, 0x50
        je      .pk_down
        jmp     .pk
.pk_ascii:
        cmp     al, 0x0D
        je      .choose
        jmp     .pk
.pk_up:
        cmp     word [opt_sel], 0
        je      .pk
        dec     word [opt_sel]
        jmp     .redraw
.pk_down:
        mov     ax, [opt_sel]
        inc     ax
        push    ax
        call    opt_count         ; -> CX
        pop     ax
        cmp     ax, cx
        jae     .pk
        mov     [opt_sel], ax
        jmp     .redraw
.cancel:
        jmp     .reset
.choose:
        ; SI -> chosen option string
        call    opt_ptr           ; -> SI = chosen option text
        mov     [cur_opt], si
        cmp     byte [ultra_mode], 0
        jne     .uchoose
        mov     si, [cur_cmd]
        call    build_cmd_opt     ; builds "PS2 <cmd> <opt>"
        jmp     .runit
.uchoose:
        call    build_cmd_ultraopt ; builds "ULTRACHG <opt>"
.runit:
        call    confirm_run
.reset:
        ; restore the default exec target / display prefix
        mov     word [exec_prog], progname
        mov     word [show_pfx], pfx_ps2
        mov     byte [ultra_mode], 0
        ret

; ============================================================================
;  confirm_run : show the command, ask Y/N, run it, pause
; ============================================================================
confirm_run:
        call    draw_confirm
.c:
        call    getkey
        cmp     al, 'y'
        je      .yes
        cmp     al, 'Y'
        je      .yes
        cmp     al, 'n'
        je      .no
        cmp     al, 'N'
        je      .no
        cmp     al, 0x1B
        je      .no
        jmp     .c
.no:
        ret
.yes:
        call    cls_body_full
        mov     dx, 0x0000
        call    gotoxy
        ; announce
        mov     dx, 0x0000
        call    gotoxy
        mov     si, msg_running
        call    puts_tty
        mov     si, cmdshow
        call    puts_tty
        mov     si, crlf
        call    puts_tty
        mov     si, crlf
        call    puts_tty
        call    do_exec
        mov     si, crlf
        call    puts_tty
        mov     si, msg_donekey
        call    puts_hint
        call    getkey
        ret

; ============================================================================
;  run_revision : run PS2 _@REVision full-screen, pause
; ============================================================================
run_revision:
        mov     si, cmd_rev
        call    build_cmd_raw
        call    cls_body_full
        mov     dx, 0x0000
        call    gotoxy
        call    do_exec
        mov     si, crlf
        call    puts_tty
        mov     si, msg_donekey
        call    puts_hint
        call    getkey
        ret

; ============================================================================
;  show_power : NATIVE live battery / AC status via the APM BIOS (INT 15h).
;  This is code ingested from PS2.EXE: it uses APM 5300 (install check) and
;  530A (get power status) directly, with no call to PS2.EXE.  Read-only.
; ============================================================================
show_power:
        ; APM installation check
        mov     ax, 0x5300
        xor     bx, bx
        int     0x15
        mov     byte [pw_ok], 1
        jnc     .have
        mov     byte [pw_ok], 0
        jmp     .status
.have:
        mov     [pw_ver], ax      ; AH=major AL=minor (APM spec version)
        mov     [pw_flags], cx
.status:
        ; APM get power status (device 1 = all batteries)
        mov     byte [pw_stat], 0
        mov     ax, 0x530A
        mov     bx, 1
        int     0x15
        jc      .draw
        mov     byte [pw_stat], 1
        mov     [pw_ac], bh
        mov     [pw_bat], bl
        mov     [pw_pct], cl
        mov     [pw_time], dx
.draw:
        call    cls_body_full
        mov     dx, 0x0000
        call    gotoxy
        mov     si, pw_hdr
        call    puts_tty
        ; APM present?
        mov     dh, 3
        mov     dl, 2
        call    gotoxy
        mov     si, pw_l_apm
        call    puts_tty
        cmp     byte [pw_ok], 0
        je      .noapm
        mov     si, pw_yes
        call    puts_tty
        ; version A.B
        mov     si, pw_l_ver
        call    puts_tty
        mov     al, [pw_ver+1]    ; major
        call    print_dec_al
        mov     al, '.'
        call    putc_tty
        mov     al, [pw_ver]      ; minor
        call    print_dec_al
        mov     al, ')'
        call    putc_tty
        jmp     .powerlines
.noapm:
        mov     si, pw_no
        call    puts_tty
        jmp     .wait
.powerlines:
        cmp     byte [pw_stat], 0
        je      .nostat
        ; AC line
        mov     dh, 5
        mov     dl, 2
        call    gotoxy
        mov     si, pw_l_ac
        call    puts_tty
        mov     al, [pw_ac]
        mov     si, pw_ac_off
        cmp     al, 0
        je      .pac
        mov     si, pw_ac_on
        cmp     al, 1
        je      .pac
        mov     si, pw_ac_bak
        cmp     al, 2
        je      .pac
        mov     si, pw_unk
.pac:   call    puts_tty
        ; Battery status
        mov     dh, 6
        mov     dl, 2
        call    gotoxy
        mov     si, pw_l_bat
        call    puts_tty
        mov     al, [pw_bat]
        mov     si, pw_b_high
        cmp     al, 0
        je      .pbat
        mov     si, pw_b_low
        cmp     al, 1
        je      .pbat
        mov     si, pw_b_crit
        cmp     al, 2
        je      .pbat
        mov     si, pw_b_chg
        cmp     al, 3
        je      .pbat
        mov     si, pw_unk
.pbat:  call    puts_tty
        ; Charge %
        mov     dh, 7
        mov     dl, 2
        call    gotoxy
        mov     si, pw_l_pct
        call    puts_tty
        mov     al, [pw_pct]
        cmp     al, 0xFF
        je      .pctunk
        call    print_dec_al
        mov     al, '%'
        call    putc_tty
        jmp     .wait
.pctunk:
        mov     si, pw_unk
        call    puts_tty
        jmp     .wait
.nostat:
        mov     dh, 5
        mov     dl, 2
        call    gotoxy
        mov     si, pw_nostat
        call    puts_tty
.wait:
        mov     dh, 10
        mov     dl, 2
        call    gotoxy
        mov     si, msg_donekey
        call    puts_hint
        call    getkey
        ret

; ============================================================================
;  show_config : NATIVE current-settings read, straight from CMOS (ports 70/71).
;  PS2 stores these in extended CMOS; reading is safe & needs no checksum.
;  Verified mappings: CLICK=0x44 bit0x10, _@STATUS=0x70, PMODE/VEXP=0x72.
; ============================================================================
show_config:
