; test_kbd : recreated PC110 keyboard; highlights each key as it is pressed.
;   Hooks INT 9 to read raw scancodes (make/break, incl. 0xE0 extended).  Press
;   and release ESC to exit.  (Runs on the graphical page set up by the wrapper.)
test_kbd:
        call    cls_body_full         ; mauve page
        mov     dx, 0x0000
        call    gotoxy
        mov     si, kb_title
        call    puts_tty
        mov     cx, KBD_N             ; clear per-key pressed state
        mov     bx, kbd_state
.z:     mov     byte [bx], 0
        inc     bx
        loop    .z
        mov     byte [kb_ext], 0
        mov     byte [kb_exit], 0
        mov     byte [kbd_wr], 0
        mov     byte [kbd_rd], 0
        call    kbd_draw_all
        ; --- hook INT 9 (keyboard IRQ1) ---
        push    es
        xor     ax, ax
        mov     es, ax
        cli
        mov     ax, [es:9*4]
        mov     [kb_oldo], ax
        mov     ax, [es:9*4+2]
        mov     [kb_olds], ax
        mov     word [es:9*4], kbd_isr
        mov     [es:9*4+2], cs
        sti
        pop     es
.loop:
.dr:    mov     al, [kbd_rd]
        cmp     al, [kbd_wr]
        je      .noev
        mov     bl, al
        xor     bh, bh
        mov     al, [kbd_ring+bx]
        inc     byte [kbd_rd]
        call    kbd_feed
        jmp     .dr
.noev:  cmp     byte [kb_rawdirty], 0    ; update the raw-scancode readout
        je      .chkx
        mov     byte [kb_rawdirty], 0
        call    kbd_showraw
.chkx:  cmp     byte [kb_exit], 0
        je      .loop
        ; --- restore INT 9 ---
        push    es
        xor     ax, ax
        mov     es, ax
        cli
        mov     ax, [kb_oldo]
        mov     [es:9*4], ax
        mov     ax, [kb_olds]
        mov     [es:9*4+2], ax
        sti
        pop     es
        ret

; kbd_isr : IRQ1 -> queue the raw scancode, EOI.  Decoding happens in the loop.
kbd_isr:
        push    ax
        push    bx
        push    ds
        mov     ax, cs
        mov     ds, ax
        in      al, 0x60
        mov     bl, [kbd_wr]
        xor     bh, bh
        mov     [kbd_ring+bx], al
        inc     byte [kbd_wr]
        mov     al, 0x20
        out     0x20, al
        pop     ds
        pop     bx
        pop     ax
        iret

; kbd_feed : AL = one raw scancode byte.
kbd_feed:
        mov     ah, [kb_raw1]        ; log the last 3 raw bytes (for the readout)
        mov     [kb_raw0], ah
        mov     ah, [kb_raw2]
        mov     [kb_raw1], ah
        mov     [kb_raw2], al
        mov     byte [kb_rawdirty], 1
        mov     ah, al               ; plain ScrollLock (0x46) -> NumLock key (0x45),
        and     ah, 0x7F             ;   but NOT extended E0 46 (that is Break)
        cmp     ah, 0x46
        jne     .noal
        cmp     byte [kb_ext], 0
        jne     .noal
        and     al, 0x80             ; keep the break bit
        or      al, 0x45
.noal:  cmp     byte [kb_e1], 0      ; swallow the rest of a Pause (E1) sequence
        je      .noe1
        dec     byte [kb_e1]
        ret
.noe1:  cmp     al, 0xE0             ; extended prefix
        jne     .n1
        mov     byte [kb_ext], 1
        ret
.n1:    cmp     al, 0xE1             ; Pause (E1 ...): also lights the Brk key, then
        jne     .n2                  ;   skips the 2 bytes that follow each E1
        mov     byte [kb_e1], 2
        mov     byte [kb_ext], 0
        mov     ah, 0x46             ; Brk = sc 0x46 ext 1 (shared with E0 46 Break form)
        mov     bl, 1                ; latch ON
        mov     bh, 1
        jmp     .dolatch
.n2:    mov     ah, al               ; ah = code (bit7 = break)
        and     ah, 0x7F
        mov     bl, 1                ; bl = pressed?
        test    al, 0x80
        jz      .p
        xor     bl, bl
.p:     mov     bh, [kb_ext]         ; bh = ext flag for this code
        mov     byte [kb_ext], 0
.dolatch:
        push    bx                   ; save pressed/ext
        xor     si, si               ; find key: sc==ah && ext==bh
        mov     di, kbd_keys
.f:     cmp     si, KBD_N
        jae     .nf
        mov     al, [di+4]           ; scancode
        cmp     al, ah
        jne     .nx
        mov     al, [di+5]           ; ext
        cmp     al, bh
        je      .found
.nx:    add     di, 8
        inc     si
        jmp     .f
.found: pop     bx                   ; bl=pressed bh=ext
        or      bl, bl
        jz      .brk                 ; release
        mov     byte [kbd_state+si], 1   ; press -> latch ON (stays lit once tested)
        call    kbd_drawkey
        ret
.brk:   cmp     ah, 0x01             ; release: leave it lit; only ESC-release exits
        jne     .done
        mov     byte [kb_exit], 1
.done:  ret
.nf:    pop     bx
        ret

; kbd_draw_all : draw every key in its current state.
kbd_draw_all:
        xor     si, si
.l:     cmp     si, KBD_N
        jae     .d
        call    kbd_drawkey
        inc     si
        jmp     .l
.d:     ret

; kbd_drawkey : SI = key index.  Fills the key (navy, or red if pressed) and
;   draws its white label centred.
kbd_drawkey:
        pusha
        mov     ax, si
        shl     ax, 3                ; idx*8 (record size)
        mov     di, kbd_keys
        add     di, ax
        mov     al, [di+1]           ; fr_x = KB_X + col*KB_U
        xor     ah, ah
        mov     bx, KB_U
        mul     bx
        add     ax, KB_X
        mov     [fr_x], ax
        mov     al, [di]             ; fr_y = KB_Y + row*KB_ROWH
        xor     ah, ah
        mov     bx, KB_ROWH
        mul     bx
        add     ax, KB_Y
        mov     [fr_y], ax
        mov     al, [di+2]           ; fr_w = w*KB_U - KB_GAP
        xor     ah, ah
        mov     bx, KB_U
        mul     bx
        sub     ax, KB_GAP
        mov     [fr_w], ax
        mov     al, [di+3]           ; fr_h = h*KB_ROWH - 8
        xor     ah, ah
        mov     bx, KB_ROWH
        mul     bx
        sub     ax, 8
        mov     [fr_h], ax
        mov     ax, [di+6]           ; label pointer
        mov     [kb_lbl], ax
        mov     bl, [kbd_state+si]
        mov     byte [fr_col], C_NAVY
        or      bl, bl
        jz      .fill
        mov     byte [fr_col], C_RED
.fill:  call    g_fillrect
        mov     si, [kb_lbl]         ; centre the label
        call    gt_strlen            ; cx = len
        mov     ax, cx
        shl     ax, 3
        mov     bx, [fr_w]
        sub     bx, ax
        sar     bx, 1
        add     bx, [fr_x]
        mov     [gt_x], bx
        mov     ax, [fr_h]
        sub     ax, 16
        sar     ax, 1
        add     ax, [fr_y]
        mov     [gt_y], ax
        mov     byte [g_col], C_WHITE
        mov     si, [kb_lbl]
        call    gtext
        popa
        ret

