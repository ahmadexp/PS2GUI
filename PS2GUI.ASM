; ============================================================================
;  test_sig : signature / inking-pad test.  The PC110 has a resistive digitizer
;  ("inking pad", driven by INKDRV.COM / INT 51h) behind an I/O window at base
;  0x15E0 (also 0x25E0/0x35E0), fed by the M38223 MCU's touch-panel ADC.  We
;  drive it directly and polled: enable (out base+2,0x38), then read 3-byte
;  packets [flags, rawX, rawY] whenever status (base+1) bit7 is set, acking each
;  byte by pulsing base+2 bit0 (exactly what INKDRV's IRQ handler does).  Packet
;  flags: bit0 = pen-down, bit3 = rawY bit8, bit4 = rawX bit8, bit7 = sync.
;  Draw the strokes so you can literally sign; any key exits (control restored).
; ============================================================================
test_sig:
        mov     ax, 0x0012            ; VGA mode 12h (640x480x16) for real drawing
        int     0x10
        call    set_palette           ; PS2GUI 5-colour theme (0=mauve 1=white 3=navy)
        mov     word [tty_col], 0x0004 ; dark (black) text so it shows on the mauve panel
        mov     dx, 0x0000
        call    gotoxy
        mov     si, ss_title
        call    puts_tty
        mov     dh, 2
        mov     dl, 2
        call    gotoxy
        mov     si, ss_inst
        call    puts_tty
        mov     word [fr_x], 32       ; signing box: navy frame + white field
        mov     word [fr_y], 64
        mov     word [fr_w], 576
        mov     word [fr_h], 340
        mov     byte [fr_col], 3
        call    g_fillrect
        mov     word [fr_x], 40
        mov     word [fr_y], 72
        mov     word [fr_w], 560
        mov     word [fr_h], 324
        mov     byte [fr_col], 1
        call    g_fillrect
        ; --- inking I/O base + IRQ vector from the PC110 BIOS (as INKDRV does)
        mov     ch, 0x96
        mov     ax, 0x5380
        mov     bx, 0x8300
        int     0x15
        mov     bl, cl               ; base = basetab[CL & 3]
        and     bx, 3
        shl     bx, 1
        mov     ax, [cs:sig_basetab+bx]
        mov     [sig_base], ax
        mov     bl, cl               ; irq  = irqtab[(CL & 0x70) >> 4]
        and     bx, 0x70
        shr     bx, 1
        shr     bx, 1
        shr     bx, 1
        shr     bx, 1
        mov     al, [cs:sig_irqtab+bx]
        add     al, 8                ; -> interrupt vector
        cmp     al, 0x10
        jb      .vok
        add     al, 0x60             ; slave-PIC IRQs vector at 0x70+
.vok:   mov     [sig_vec], al
        mov     byte [sig_idx], 0
        mov     word [sig_pkts], 0
        mov     word [sig_ctr], 0
        mov     byte [sig_wr], 0
        mov     byte [sig_rd], 0
        call    sig_status
        ; --- install our ISR on the inking IRQ vector
        mov     al, [sig_vec]
        xor     ah, ah
        mov     bx, ax
        shl     bx, 1
        shl     bx, 1                ; bx = vec*4
        push    es
        xor     ax, ax
        mov     es, ax
        cli
        mov     ax, [es:bx]
        mov     [sig_oldveco], ax
        mov     ax, [es:bx+2]
        mov     [sig_oldvecs], ax
        mov     word [es:bx], sig_isr
        mov     [es:bx+2], cs
        pop     es
        ; --- unmask that IRQ in the PIC (remember reg+bit to restore)
        mov     al, [sig_vec]
        cmp     al, 0x70
        jb      .mmaster
        sub     al, 0x70             ; slave bit 0..7
        mov     word [sig_mport], 0x00A1
        jmp     .mhave
.mmaster:
        sub     al, 8                ; master bit 0..7
        mov     word [sig_mport], 0x0021
.mhave: mov     cl, al
        mov     al, 1
        shl     al, cl
        mov     [sig_mbit], al
        mov     dx, [sig_mport]
        in      al, dx
        mov     [sig_omask], al
        mov     bl, [sig_mbit]
        not     bl
        and     al, bl               ; clear bit = unmask
        out     dx, al
        ; --- save control byte, enable reporting, run
        mov     dx, [sig_base]
        add     dx, 2
        in      al, dx
        mov     [sig_octl], al
        mov     al, 0x38
        out     dx, al
        sti
.loop:
.drain: mov     al, [sig_rd]
        cmp     al, [sig_wr]
        je      .idle
        mov     bl, al
        xor     bh, bh
        mov     al, [sig_ring+bx]
        inc     byte [sig_rd]
        call    sig_feed
        jmp     .drain
.idle:  inc     word [sig_ctr]       ; throttle the on-screen readout
        test    word [sig_ctr], 0x0FFF
        jnz     .kchk
        call    sig_status
.kchk:  mov     ah, 1
        int     0x16
        jz      .loop
        xor     ah, ah
        int     0x16
        cmp     al, 'c'              ; C = run the 4-corner calibration
        je      .docal
        cmp     al, 'C'
        je      .docal
        jmp     .exit
.docal: call    sig_calibrate
        jmp     .loop
.exit:
        ; --- cleanup: disable pad, restore PIC mask + IRQ vector
        cli
        mov     dx, [sig_base]
        add     dx, 2
        mov     al, [sig_octl]
        out     dx, al
        mov     dx, [sig_mport]
        mov     al, [sig_omask]
        out     dx, al
        mov     al, [sig_vec]
        xor     ah, ah
        mov     bx, ax
        shl     bx, 1
        shl     bx, 1
        push    es
        xor     ax, ax
        mov     es, ax
        mov     ax, [sig_oldveco]
        mov     [es:bx], ax
        mov     ax, [sig_oldvecs]
        mov     [es:bx+2], ax
        pop     es
        sti
        mov     word [tty_col], 0x0007 ; restore default teletype colour
        ret

; inking IRQ handler: read one byte from base+0, ring it, ack (pulse base+2 bit0),
; EOI.  Assembly/decoding happens in the main loop (sig_feed).
sig_isr:
        push    ax
        push    bx
        push    dx
        push    ds
        mov     ax, cs
        mov     ds, ax
        mov     dx, [sig_base]
        in      al, dx               ; data byte at base+0
        mov     [sig_dd], al
        mov     bl, [sig_wr]
        xor     bh, bh
        mov     [sig_ring+bx], al
        inc     byte [sig_wr]
        mov     dx, [sig_base]
        add     dx, 2                ; ack: pulse base+2 bit0
        in      al, dx
        mov     ah, al
        or      al, 1
        out     dx, al
        mov     al, ah
        and     al, 0xFE
        out     dx, al
        mov     al, 0x20             ; EOI
        cmp     byte [sig_vec], 0x70
        jb      .meoi
        out     0xA0, al
.meoi:  out     0x20, al
        pop     ds
        pop     dx
        pop     bx
        pop     ax
        iret

sig_feed:                             ; AL = one inking-data byte
        mov     bl, [sig_idx]
        or      bl, bl
        jnz     .b1
        test    al, 0x80              ; byte-0 sync bit
        jz      .ret
        mov     [sig_b0], al
        mov     byte [sig_idx], 1
        ret
.b1:    cmp     bl, 1
        jne     .b2
        mov     [sig_b1], al
        mov     byte [sig_idx], 2
        ret
.b2:    mov     [sig_b2], al
        mov     byte [sig_idx], 0
        inc     word [sig_pkts]
        call    sig_plot
.ret:   ret

sig_plot:                             ; decode packet -> plot + readout
        mov     al, [sig_b1]          ; rawX = b1[0..6] | (b0.4 -> bit7)
        and     al, 0x7F
        test    byte [sig_b0], 0x10
        jz      .nx
        or      al, 0x80
.nx:    mov     ah, 0
        mov     [sig_rx], ax
        mov     al, [sig_b2]          ; rawY = b2[0..6] | (b0.3 -> bit7)
        and     al, 0x7F
        test    byte [sig_b0], 0x08
        jz      .ny
        or      al, 0x80
.ny:    mov     ah, 0
        mov     [sig_ry], ax
        cmp     byte [sig_cal], 0     ; calibration capture: decode only, don't draw
        jne     .done
        ; --- map raw -> screen X (calibrated if available, else default) ---
        mov     ax, [sig_rx]
        cmp     byte [cal_valid], 0
        jne     .calx
        shl     ax, 1                 ; default: 40 + rawX*2
        add     ax, 40
        jmp     .hx
.calx:  mov     bx, [cal_xl]
        mov     cx, [cal_xr]
        mov     word [cal_span], 560
        call    cal_map
        add     ax, 40
.hx:    mov     [sig_sx], ax
        ; --- map raw -> screen Y ---
        mov     ax, [sig_ry]
        cmp     byte [cal_valid], 0
        jne     .caly
        add     ax, 72                ; default: 72 + rawY
        jmp     .hy
.caly:  mov     bx, [cal_yt]
        mov     cx, [cal_yb]
        mov     word [cal_span], 324
        call    cal_map
        add     ax, 72
.hy:    mov     [sig_sy], ax
        test    byte [sig_b0], 1      ; pen down?
        jz      .rd
        mov     al, 3                 ; navy ink, 3x3 block
        mov     dx, [sig_sy]
        mov     bp, 3
.yl:    mov     cx, [sig_sx]
        push    bp
        mov     bp, 3
.xl:    call    putpixel
        inc     cx
        dec     bp
        jnz     .xl
        pop     bp
        inc     dx
        dec     bp
        jnz     .yl
.rd:    test    word [sig_pkts], 0x0F ; refresh readout every 16 packets
        jnz     .done
        call    sig_status
.done:  ret

sig_status:
        ; --- line 1: raw register monitor ---
        mov     dh, 27
        mov     dl, 2
        call    gotoxy
        mov     si, ss_base
        call    puts_tty
        mov     ax, [sig_base]        ; base as 4 hex digits
        mov     al, ah
        call    print_hex_al
        mov     ax, [sig_base]
        call    print_hex_al
        mov     si, ss_st
        call    puts_tty
        mov     al, [sig_vec]
