; ============================================================================
;  Mouse (INT 33h) + a custom Easy-Setup arrow cursor drawn by hand so it is
;  visible (white body, black outline) on any tile colour.
; ============================================================================
; mouse_init : if an INT 33h driver is present use it (mode 1); otherwise talk to
; the PS/2 trackpad (U75 uPD17137A) directly through the 8042 aux channel (mode 2).
mouse_init:
        push    es
        xor     ax, ax
        mov     es, ax
        mov     bx, [es:0x33*4]      ; INT 33h vector
        mov     dx, [es:0x33*4+2]
        pop     es
        mov     ax, bx
        or      ax, dx
        jz      .direct              ; no driver -> talk to the trackpad ourselves
        xor     ax, ax
        int     0x33                 ; reset; AX=FFFF if installed
        cmp     ax, 0xFFFF
        jne     .direct
        mov     ax, 7                ; x range (leave 16px for the cursor)
        xor     cx, cx
        mov     dx, 609
        int     0x33
        mov     ax, 8                ; y range
        xor     cx, cx
        mov     dx, 449
        int     0x33
        mov     ax, 4                ; start near the centre
        mov     cx, 300
        mov     dx, 216
        int     0x33
        mov     byte [have_mouse], 1
        mov     byte [mouse_mode], 1
        ret
.direct:
        call    mouse_init_direct
        ret

; ---- extra 8042 helpers (aux_wait_ibf / aux_get / aux_write already exist) ----
k_cmd:                               ; AL -> command port 0x64
        push    ax
        call    aux_wait_ibf
        pop     ax
        out     0x64, al
        ret
k_data:                              ; AL -> data port 0x60
        push    ax
        call    aux_wait_ibf
        pop     ax
        out     0x60, al
        ret
k_flush:                             ; drain any pending 8042 output bytes
        push    ax
        push    cx
        mov     cx, 24
.f:     in      al, 0x64
        test    al, 1
        jz      .d
        in      al, 0x60
        loop    .f
.d:     pop     cx
        pop     ax
        ret
aux_drain:                           ; read replies until none arrive (per-byte wait)
        push    ax
        push    cx
.d:     mov     cx, 0x1000           ; short wait for the next byte
.w:     in      al, 0x64
        test    al, 1
        jnz     .g
        loop    .w
        pop     cx
        pop     ax
        ret
.g:     in      al, 0x60
        jmp     .d
aux_get_slow:                        ; aux_get with a much longer timeout -> AL, CF=1
        push    cx
        push    dx
        mov     dx, 32
.o:     xor     cx, cx
.w:     in      al, 0x64
        test    al, 1
        jnz     .g
        loop    .w
        dec     dx
        jnz     .o
        pop     dx
        pop     cx
        stc
        ret
.g:     in      al, 0x60
        pop     dx
        pop     cx
        clc
        ret

; mouse_init_direct : bring up the PS/2 trackpad and take IRQ12.
mouse_init_direct:
        mov     word [ms_x], 300
        mov     word [ms_y], 216
        mov     byte [mpkt_idx], 0
        cli                          ; ints off: the BIOS ISR must not steal our replies
        call    k_flush
        mov     al, 0x20             ; read 8042 command byte
        call    k_cmd
        call    aux_get_slow
        jc      .no                  ; 8042 not responding at all
        mov     [old_kbc], al
        mov     al, 0xA8             ; enable aux clock
        call    k_cmd
        mov     al, 0xF5             ; disable reporting FIRST: stop any active stream
        call    aux_write            ; (a finger on the pad during init would otherwise
        call    aux_get_slow         ;  interleave packet bytes with the command ACKs and
        call    aux_drain            ;  leave the device half-configured -> flaky start)
        call    k_flush
        mov     al, 0xF6             ; set defaults
        call    aux_write
        call    aux_drain
        mov     al, 0xF2             ; get device ID (presence check)
        call    aux_write
        call    aux_get_slow         ; FA
        jc      .no                  ; no aux device present
        call    aux_drain            ; ID byte
        mov     al, 0xF3             ; set sample rate 100/s
        call    aux_write
        call    aux_drain
        mov     al, 0x64
        call    aux_write
        call    aux_drain
        mov     al, 0xF4             ; enable data reporting (stream mode)
        call    aux_write
        call    aux_drain
        call    k_flush              ; OBF empty before enabling IRQ12
        mov     byte [mpkt_idx], 0
        cli                          ; install IRQ12 (INT 0x74) handler
        xor     ax, ax
        mov     es, ax
        mov     ax, [es:0x74*4]
        mov     [old_i12_off], ax
        mov     ax, [es:0x74*4+2]
        mov     [old_i12_seg], ax
        mov     word [es:0x74*4], irq12_handler
        mov     ax, cs
        mov     [es:0x74*4+2], ax
        mov     al, 0x60             ; 8042 command byte: enable kbd+aux + both IRQs
        call    k_cmd
        mov     al, [old_kbc]
        or      al, 0x03             ; kbd int + aux int on
        and     al, 0xCF             ; kbd + aux clocks enabled (clear disable bits)
        call    k_data
        call    k_flush              ; drain any packet byte queued while enabling the aux
        mov     byte [mpkt_idx], 0   ; IRQ so the very first IRQ12 lands on a true byte0
        in      al, 0xA1             ; unmask IRQ12 (slave PIC bit4)
        and     al, 0xEF
        out     0xA1, al
        in      al, 0x21             ; unmask cascade IRQ2 (master bit2)
        and     al, 0xFB
        out     0x21, al
        sti
        ; Seed the button baseline as "pressed" so a phantom button state left in
        ; the trackpad/8042 across an exit->relaunch is NOT read as a fresh click
        ; (a fresh right-click = ESC = instant quit).  A real click still needs a
        ; release first, which always happens naturally.
        mov     byte [ms_pbtn], 1
        mov     byte [ms_prbtn], 1
        mov     byte [have_mouse], 1
        mov     byte [mouse_mode], 2
        ret
.no:    mov     byte [have_mouse], 0
        mov     byte [mouse_mode], 0
        ret

; irq12_handler : one aux byte per interrupt; assemble 3-byte packets.
irq12_handler:
        pusha
        push    ds
        mov     ax, cs
        mov     ds, ax
        in      al, 0x60
        mov     bl, [mpkt_idx]
        or      bl, bl
        jnz     .b1
        test    al, 0x08             ; byte0 sync bit must be set
        jz      .eoi
        mov     [mpkt0], al
        mov     byte [mpkt_idx], 1
        jmp     .eoi
.b1:    cmp     bl, 1
        jne     .b2
        mov     [mpkt1], al
        mov     byte [mpkt_idx], 2
        jmp     .eoi
.b2:    mov     [mpkt2], al
        mov     byte [mpkt_idx], 0
        call    mpkt_apply
.eoi:   mov     al, 0x20
        out     0xA0, al             ; EOI slave PIC
        out     0x20, al             ; EOI master PIC
        pop     ds
        popa
        iret

; mpkt_apply : decode [mpkt0..2] into ms_x/ms_y/mbtns
mpkt_apply:
        mov     al, [mpkt0]          ; buttons: bit0 left, bit1 right
        and     al, 3
        xor     ah, ah
        mov     [mbtns], ax
        mov     al, [mpkt1]          ; X delta (signed)
        cbw
        add     ax, [ms_x]
        jns     .xhi
        xor     ax, ax
        jmp     .xok
.xhi:   cmp     ax, 608              ; 640 - CUR_W(32): keep the whole cursor on-screen
        jbe     .xok                 ;   (an off-screen cursor -> BIOS pixel I/O past the
        mov     ax, 608              ;    screen -> VGA-BIOS fault -> crash)
.xok:   mov     [ms_x], ax
        mov     al, [mpkt2]          ; Y delta (signed); screen Y grows down
        cbw
        neg     ax
        add     ax, [ms_y]
        jns     .yhi
        xor     ax, ax
        jmp     .yok
.yhi:   cmp     ax, 448              ; 480 - CUR_H(32): keep the whole cursor on-screen
        jbe     .yok
        mov     ax, 448
.yok:   mov     [ms_y], ax
        ret

; mouse_shutdown : undo the direct-mode setup before returning to DOS.
mouse_shutdown:
        cmp     byte [mouse_mode], 2
        jne     .d
        cli
        call    k_flush
        mov     al, 0xF5             ; disable reporting
        call    aux_write
        call    aux_get
        in      al, 0xA1             ; mask IRQ12
        or      al, 0x10
        out     0xA1, al
        xor     ax, ax               ; restore IVT 0x74
        mov     es, ax
        mov     ax, [old_i12_off]
        mov     [es:0x74*4], ax
        mov     ax, [old_i12_seg]
        mov     [es:0x74*4+2], ax
        mov     al, 0x60             ; restore 8042 command byte
        call    k_cmd
        mov     al, [old_kbc]
        call    k_data
        sti
.d:     ret

; wait_event : block until a keystroke, a left click, or a right click, keeping
;   the cursor live.  Returns [ev]=0 (key in [ev_key]) or [ev]=1 (left click at
;   [ms_x],[ms_y]).  Right click is reported as an ESC keystroke.
wait_event:
        cmp     byte [have_mouse], 0
        jne     .spin
        call    getkey
        mov     [ev_key], ax
        mov     byte [ev], 0
        ret
.spin:
        cmp     byte [mouse_mode], 2 ; direct mode: ms_x/ms_y/mbtns updated by IRQ12
        je      .havepos
        mov     ax, 3
        int     0x33                 ; BX=buttons CX=x DX=y
        mov     [ms_x], cx
        mov     [ms_y], dx
        mov     [mbtns], bx
.havepos:
        cmp     byte [cur_valid], 0
        je      .upd
        mov     ax, [ms_x]
        cmp     ax, [cur_lx]
        jne     .upd
        mov     ax, [ms_y]
        cmp     ax, [cur_ly]
        jne     .upd
        jmp     .btn
.upd:
        inc     byte [cur_fctr]      ; advance the wing-flap animation as we move
        mov     al, [cur_fctr]
        shr     al, 1
        shr     al, 1                ; flip frame every 4 move steps (~steady flap)
        and     al, 1
        mov     [cur_frame], al
        cmp     byte [cur_valid], 0
        je      .snap
        call    cursor_restore       ; erase at the OLD cur_lx/cur_ly
.snap:
        cli                          ; snapshot the (volatile) position atomically
        mov     ax, [ms_x]
        mov     [cur_lx], ax
        mov     ax, [ms_y]
        mov     [cur_ly], ax
        sti
        call    cursor_save          ; save/draw use the stable cur_lx/cur_ly so a
        call    cursor_draw          ; mid-save IRQ12 update can't smear the buffer
        mov     byte [cur_valid], 1
.btn:
        mov     ax, [mbtns]
        and     ax, 1
        mov     bl, [ms_pbtn]
        mov     [ms_pbtn], al
        test    al, al
        jz      .rbtn
        test    bl, bl
        jnz     .rbtn
        mov     byte [ev], 1
        ret
.rbtn:
        mov     ax, [mbtns]
        and     ax, 2
        mov     bl, [ms_prbtn]
        mov     [ms_prbtn], al
        test    al, al
        jz      .key
        test    bl, bl
        jnz     .key
        mov     word [ev_key], 0x011B    ; ESC
        mov     byte [ev], 0
        ret
.key:
        mov     ah, 1
        int     0x16
        jz      .spin
        xor     ah, ah
        int     0x16
        mov     [ev_key], ax
        mov     byte [ev], 0
        ret

CUR_BW  equ 5                           ; bytes/row covering the 30px cursor + bit slack

cur_winoff:                          ; [cur_off] = cur_ly*80 + cur_lx/8
        push    ax
        push    bx
        push    dx
        mov     ax, [cur_ly]
        mov     dx, 80
        mul     dx
        mov     bx, [cur_lx]
        shr     bx, 3
        add     ax, bx
        mov     [cur_off], ax
        pop     dx
        pop     bx
        pop     ax
        ret

; cursor_save : copy the cursor's byte-aligned window (all 4 planes) to cur_buf,
;   directly from VRAM (fast — no per-pixel BIOS calls -> almost no flicker).
cursor_save:                         ; save ONLY silhouette pixels via BIOS (INT 10h)
        pusha
        mov     di, cur_buf
        xor     bp, bp               ; row
.rl:    mov     ax, bp               ; si -> this row's silhouette words
        mov     cx, CUR_WW * 2
        mul     cx
        mov     si, cur_silh
        add     si, ax
        xor     bx, bx               ; col
.cl:    mov     ax, bx               ; test silhouette bit for this column
        shr     ax, 4
        shl     ax, 1
        push    si
        add     si, ax
        mov     dx, [si]
        pop     si
        mov     cx, bx
        and     cx, 15
        mov     ax, 0x8000
        shr     ax, cl
        test    dx, ax
        jz      .nxt
        mov     cx, [cur_lx]
        add     cx, bx
        mov     dx, [cur_ly]
        add     dx, bp
        mov     ah, 0x0D
        xor     bh, bh
        push    bp
        push    si
        push    di
        push    bx
        int     0x10                 ; AL = pixel colour
        pop     bx
        pop     di
        pop     si
        pop     bp
        mov     [di], al
        inc     di
.nxt:   inc     bx
        cmp     bx, CUR_W
        jb      .cl
        inc     bp
        cmp     bp, CUR_H
        jb      .rl
        popa
        ret

; cursor_restore : write cur_buf back to VRAM (all 4 planes), directly & fast.
cursor_restore:                      ; restore ONLY silhouette pixels via BIOS
        pusha
        mov     di, cur_buf          ; source index
        xor     bp, bp               ; row
.rl:    mov     ax, bp               ; si -> this row's silhouette words
        mov     cx, CUR_WW * 2
        mul     cx
        mov     si, cur_silh
        add     si, ax
        xor     bx, bx               ; col
.cl:    mov     ax, bx               ; test silhouette bit for this column
        shr     ax, 4
        shl     ax, 1
        push    si
        add     si, ax
        mov     dx, [si]
        pop     si
        mov     cx, bx
        and     cx, 15
        mov     ax, 0x8000
        shr     ax, cl
        test    dx, ax
        jz      .nxt
        mov     al, [di]             ; saved colour
        inc     di
        mov     cx, [cur_lx]
        add     cx, bx
        mov     dx, [cur_ly]
        add     dx, bp
        push    bp
        push    si
        push    di
        push    bx
        call    putpixel
        pop     bx
        pop     di
        pop     si
        pop     bp
.nxt:   inc     bx
        cmp     bx, CUR_W
        jb      .cl
        inc     bp
        cmp     bp, CUR_H
        jb      .rl
        popa
        ret

; cursor_draw : draw the duck at (cur_lx,cur_ly).  Picks the wing frame
;   (0 = up/glide, 1 = down) so it flaps as it moves.  Uses the BIOS bitmap
;   path (no VGA Set/Reset — that faulted on the real F65535 hardware).
cursor_draw:
        mov     si, cur_blk0         ; select frame bitmaps
        mov     di, cur_wht0
        cmp     byte [cur_frame], 0
        je      .have
        mov     si, cur_blk1
        mov     di, cur_wht1
.have:  mov     [cur_bp], si
        mov     [cur_wp], di
        mov     ax, [cur_lx]
        mov     [bm_x], ax
        mov     ax, [cur_ly]
        mov     [bm_y], ax
        mov     word [bm_ww], CUR_WW
        mov     word [bm_h], CUR_H
        mov     si, [cur_bp]         ; dark outline
        mov     byte [g_col], C_BLACK
        call    draw_bmp
        mov     ax, [cur_lx]
        mov     [bm_x], ax
        mov     ax, [cur_ly]
        mov     [bm_y], ax
        mov     word [bm_ww], CUR_WW
        mov     word [bm_h], CUR_H
        mov     si, [cur_wp]         ; white body
        mov     byte [g_col], C_WHITE
        call    draw_bmp
        ret

; cursor_hide : if the cursor is drawn, erase it (restore bg) and mark invalid.
; Call before an incremental (partial) redraw so no ghost is left behind.
cursor_hide:
        cmp     byte [cur_valid], 0
        je      .d
        call    cursor_restore
        mov     byte [cur_valid], 0
.d:     ret

; main_tile / sub_tile : repaint one tile [tile_i] with [tile_sel] (used for the
; incremental selection change, so navigation doesn't repaint the whole screen).
main_tile:
        mov     bx, [tile_i]
        shl     bx, 1
        mov     si, [cat_tile+bx]
        call    g_tile
        ret
sub_tile:
        mov     al, [cat_start]
        xor     ah, ah
        add     ax, [tile_i]
        shl     ax, 1
        mov     bx, ax
        mov     si, [row_icon+bx]
        call    g_tile
        ret

; hit_tile : CF=0 + AX=grid index if (ms_x,ms_y) is on a tile, else CF=1
hit_tile:
        mov     ax, [ms_x]
        sub     ax, 80
        js      .miss
        xor     dx, dx
        mov     bx, 104
        div     bx
        cmp     dx, 64
        jae     .miss
        cmp     ax, GCOLS
        jae     .miss
        mov     [hit_col], ax
        mov     ax, [ms_y]
        sub     ax, 160
        js      .miss
        xor     dx, dx
        mov     bx, 112
        div     bx
        cmp     dx, 64
        jae     .miss
        mov     bx, GCOLS
        mul     bx
        add     ax, [hit_col]
        clc
        ret
.miss:  stc
        ret

; hit_opt : CF=0 + AX=option index if (ms_x,ms_y) is on a picker row, else CF=1
hit_opt:
        mov     ax, [ms_x]
        cmp     ax, PK_X
        jb      .miss
        cmp     ax, PK_X + PK_W
        jae     .miss
        mov     ax, [ms_y]
        sub     ax, [pk_y]
        sub     ax, PK_TITLE + 4
        js      .miss
        xor     dx, dx
        mov     bx, PK_ROW
        div     bx
        cmp     ax, [pkd_n]
        jae     .miss
        clc
        ret
.miss:  stc
        ret

; ---- draw_icon : SI=16-word icon, [ic_x]/[ic_y] = top-left; white tile + red art.
; Rows 0/1/14/15 are blank in every icon, so the row's selection box (cell edges)
; and the 1px inter-row gaps never clip the art. ----
draw_icon:
        pusha
        xor     bp, bp               ; r = 0..15
.rl:    cmp     bp, 16
        jae     .done
        mov     ax, bp
        shl     ax, 1
        mov     bx, si
        add     bx, ax
        mov     dx, [bx]             ; row bit pattern
        xor     di, di               ; c = 0..15
.cl:    cmp     di, 16
        jae     .cnext
        shl     dx, 1                ; MSB -> CF
        mov     al, C_WHITE
        jnc     .plot
        mov     al, C_RED
.plot:
        push    dx                   ; preserve remaining bits
        mov     cx, [ic_x]
        add     cx, di
        mov     dx, [ic_y]
        add     dx, bp
        call    putpixel             ; AL=colour CX=x DX=y
        pop     dx
        inc     di
        jmp     .cl
.cnext:
        inc     bp
        jmp     .rl
.done:
        popa
        ret

